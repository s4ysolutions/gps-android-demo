    @Test
    fun reset_shouldNotCrash() {
        // Arrange
        val filter = GPSFilter.Kalman.ConstantPosition.instance
        val ts = System.currentTimeMillis()
        // Act
        filter.reset(gpsPosition0, defaultDt)
    }
    @Test
    fun reset_shouldProvideFirstEstimation() {
        // Arrange
        val filter = GPSFilter.Kalman.ConstantPosition.instance
        val ts = System.currentTimeMillis()
        filter.reset(gpsPosition0, defaultDt)
        // Act
        val estimation = StateVector.LongitudeLatitude(filter.kalmanFilter.stateEstimation)
        // Assert
        assertThat(estimation.latitude.degree).isWithin(1e-6).of(latitude0)
        assertThat(estimation.longitude.degree).isWithin(1e-6).of(longitude0)
    }
    @Test
    fun apply_shouldNotCrash() {
        val filter = GPSFilter.Kalman.ConstantPosition.instance
        val ts = System.currentTimeMillis()
        filter.reset(gpsPosition0, defaultDt)
        val gpsPosition1 = GPSUpdate(latitude0 + 1e04, longitude0 + 1e04, velocity0, gpsAccuracy0, bearing0d, ts + defaultDt)
        // Act
        filter.apply(gpsPosition1)
    }

